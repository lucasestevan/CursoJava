package Controle;

public class SwitchSemBreak {

    public static void main(String[] args) {

        String faixa = "amarela";

        switch (faixa.toLowerCase()) {
            case "preta":
                System.out.println("Sei o bassai da faixa preta");
            case "marrom":
                System.out.println("Sei o bassai da faixa marrom");
            case "roxa":
                System.out.println("Sei o bassai da faixa roxa");
            case "verde":
                System.out.println("Sei o bassai da faixa verde");
            case "amarela":
                System.out.println("Sei o bassai da faixa amarela");


        }
    }
}

